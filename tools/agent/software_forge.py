#!/usr/bin/env python3
"""
Software Forge — AI Distro's code generation and project scaffolding engine.

Capabilities:
  1. Generate standalone scripts (Python, Bash, Rust)
  2. Scaffold full projects (Flask web apps, CLI tools, systemd services)
  3. Fix and iterate on existing code
  4. Auto-register generated tools as dynamic skills
  5. Sandboxed execution with output capture

Uses the local LLM (brain.py) for code generation and validates output
before registering as a new skill.
"""
import json
import os
import stat
import subprocess
import sys
import tempfile
import textwrap
from pathlib import Path

# Directories
CUSTOM_TOOLS_DIR = Path(os.path.expanduser("~/AI_Distro/tools/agent/custom"))
DYNAMIC_SKILLS_DIR = Path(os.path.expanduser("~/AI_Distro/src/skills/dynamic"))
PROJECTS_DIR = Path(os.path.expanduser("~/ai-distro-projects"))
BRAIN_PATH = Path(os.path.dirname(__file__)) / "brain.py"


# ── Code Generation via LLM ──────────────────────────────────────────────

def generate_code_with_llm(description, language="python"):
    """Ask the brain to generate code for a given description."""
    prompt = textwrap.dedent(f"""\
        Write a complete, working {language} program that does the following:
        {description}

        Requirements:
        - The code must be complete and runnable
        - Include proper error handling
        - Include a main entry point
        - Use standard library modules where possible
        - Do NOT include any markdown formatting, just raw code
        - Do NOT include ```python or ``` markers

        Output ONLY the code, nothing else.
    """)

    try:
        result = subprocess.run(
            [sys.executable, str(BRAIN_PATH), prompt],
            capture_output=True, text=True, timeout=60
        )
        if result.returncode == 0:
            output = result.stdout.strip()
            # The brain returns JSON; extract the payload
            try:
                data = json.loads(output)
                code = data.get("payload", output)
                # Clean any markdown artifacts
                code = _clean_code(code)
                return code
            except json.JSONDecodeError:
                return _clean_code(output)
    except Exception:
        pass
    return None


def _clean_code(code):
    """Remove markdown code fences if present."""
    lines = code.strip().split("\n")
    # Remove leading/trailing ``` markers
    if lines and lines[0].startswith("```"):
        lines = lines[1:]
    if lines and lines[-1].strip() == "```":
        lines = lines[:-1]
    return "\n".join(lines)


